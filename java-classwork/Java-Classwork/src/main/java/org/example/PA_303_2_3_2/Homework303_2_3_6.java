package org.example.PA_303_2_3_2;

public class Homework303_2_3_6 {
    public static void main(String[] args) {
      //6.	Write a program that demonstrates at least three ways to
        // increment a variable by 1 and does this multiple times.
        // Assign a value to an integer variable, print it,
        // increment by 1, print it again, increment by 1, and then print it again.

        int i = 7;
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i+1);
        i +=1;
        System.out.println(i);

    }
}
